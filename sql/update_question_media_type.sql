-- ============================================
-- 更新 question_media 表的 media_type 字段定义
-- 扩展媒体类型，支持题目相关的媒体文件
-- ============================================

-- 修改 media_type 字段的注释，扩展类型定义
-- 注意：此表只存储题目相关的媒体类型（1-6）
ALTER TABLE `question_media` 
MODIFY COLUMN `media_type` TINYINT NOT NULL COMMENT '媒体类型：
1-题目媒体（题目内容相关的图片、视频等）
2-选项媒体（选项相关的音频、图片等）
3-辅助识图
4-题目音频（题目本身的音频，如听力题的题目音频）
5-讲解音频（答题讲解的音频）
6-讲解图片（答题讲解的图片）';

-- 说明：
-- 1. 此脚本只修改字段注释，不影响现有数据
-- 2. 执行后，后端代码查询 media_type=4 时才能正确工作
-- 3. 需要在后台管理系统中上传题目音频时，设置 media_type=4
-- 4. 试卷相关的媒体（卷别音频、大题音频、中场音频等）存储在其他表中


